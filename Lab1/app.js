// Nhập vào 2 số nguyên từ bàn phím
let a = prompt("Nhập số nguyên thứ nhất:");
let b = prompt("Nhập số nguyên thứ hai:");

// Chuyển đổi chuỗi sang số và tính tổng
let c = parseInt(a) + parseInt(b);
// In ra kết quả bằng document.write và console.log
document.write(`Tổng của ${a} và ${b} là: ${c}`);
console.log(`Tổng của ${a} và ${b} là: ${c}`);
